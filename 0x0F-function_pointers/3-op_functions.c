#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>


/**
 * op_add - add two numbers
 * @a: first number
 * @b:second number
 * Return: the sum result
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - sub two numbers
 * @a: first number
 * @b:second number
 * Return: the sub result
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - mul two numbers
 * @a: first number
 * @b:second number
 * Return: the mul result
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - div two numbers
 * @a: first number
 * @b:second number
 * Return: the div result
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - mod two numbers
 * @a: first number
 * @b:second number
 * Return: the mod result
 */

int op_mod(int a, int b)
{
return (a % b);
}
